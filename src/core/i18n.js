export const SUPPORTED_LANGS = ['en', 'zh'];

// 把 'auto' / 未知值解析成具体语言。auto 跟随浏览器(zh* → zh,其余 → en)。
export function resolveLang(pref, navLang) {
  if (pref === 'en' || pref === 'zh') return pref;
  const nav = (navLang || (typeof navigator !== 'undefined' ? navigator.language : '') || 'en').toLowerCase();
  return nav.startsWith('zh') ? 'zh' : 'en';
}

// 生成绑定某语言的 t()。缺键回退到 en,再回退到键名本身。
export function makeT(lang) {
  const table = DICT[lang] || DICT.en;
  return function t(key, params) {
    let v = table[key];
    if (v === undefined) v = DICT.en[key];
    if (v === undefined) return key;
    return typeof v === 'function' ? v(params || {}) : v;
  };
}
